package entity;

public class EvoInfo {
    /**
     * algebra
     */
    private Integer algebra;
    /**
     * living cells
     */
    private Integer livingCells;

    public Integer getAlgebra() {
        return algebra;
    }

    public void setAlgebra(Integer algebra) {
        this.algebra = algebra;
    }

    public Integer getLivingCells() {
        return livingCells;
    }

    public void setLivingCells(Integer livingCells) {
        this.livingCells = livingCells;
    }
}
